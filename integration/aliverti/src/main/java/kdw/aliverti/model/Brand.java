package kdw.aliverti.model;

public class Brand {
	int id;
	String Name;
	
	public int getId() {
		return id;
	}
	public Brand setId(int id) {
		this.id = id;
		return this;
	}
	public String getName() {
		return Name;
	}
	public Brand setName(String name) {
		Name = name;
		return this;
	}
}
